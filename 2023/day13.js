function findSymmetry(lines) {
	for (let i = 0; i < lines.length - 1; i++) {
		let swapped = false;
		let d = diffBetween(lines[i], lines[i + 1]);

		if (d == 0 || (d == 1 && !swapped)) {
			if (d == 1) swapped = true;
			let valid = true;
			if (i == 0 && swapped) return 0;
			if (i == lines.length - 2 && swapped) return lines.length - 2;
			for (let j = 1; i - j >= 0 && i + 1 + j < lines.length; j++) {
				let d2 = diffBetween(lines[i - j], lines[i + 1 + j]);
				if (d2 > 1 || (d2 == 1 && swapped)) {
					// console.log(lines[i - j]);
					valid = false;
					break;
				}
				if (d2 == 1) swapped = true;
			}
			if (valid && swapped) return i;
		}
	}
	return -1;
}

function diffBetween(a, b) {
	// return true if a and b are the same except for one character
	let diffs = 0;
	for (let i = 0; i < a.length; i++) {
		if (a.charAt(i) != b.charAt(i)) diffs++;
	}
	return diffs;
}
